function skillsMembers() {
    var skills = document.getElementById("skills");
    var members = document.getElementById("members");
    var skillsMembers = document.getElementById("skillsMembers");
    var membersSkills = document.getElementById("membersSkills");
    var skillsMembersButton = document.getElementById("skillsMembersButton");
    var membersSkillsButton = document.getElementById("membersSkillsButton");
    skills.style.display = "block";
    members.style.display = "none";
    skillsMembers.style.display = "block";
    membersSkills.style.display = "none";
    skillsMembersButton.style.backgroundColor = "#f1f1f1";
    membersSkillsButton.style.backgroundColor = "#fff";
}